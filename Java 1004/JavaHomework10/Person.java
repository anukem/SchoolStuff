public class Person {

	//Instance Variables
	private String name;
	private int yearOfBirth;

	//Construtor
	public Person(String name, int year) {


		this.name = name;
		yearOfBirth = year;
	}

	//Accessor Method 
	public String toString() {

		return name;
	}
}